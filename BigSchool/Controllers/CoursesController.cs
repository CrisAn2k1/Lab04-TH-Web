        // POST: Courses/Create
        [Authorize]
        [HttpPost]
        [ValidateAntiForgeryToken]
        public ActionResult Create(Course objCourse)
        {
            BigSchoolContext context = new BigSchoolContext();

            if (!ModelState.IsValid)
            {
                objCourse.listCategory = context.Category.ToList();
                return View("Create", objCourse);
            }


            var dateTimeString = Request["txtDate"].ToString() + " " + Request["txtTime"].ToString();
            objCourse.DateTime = DateTime.Parse(dateTimeString);

            ApplicationUser user = System.Web.HttpContext.Current.GetOwinContext().GetUserManager<ApplicationUserManager>().FindById(System.Web.HttpContext.Current.User.Identity.GetUserId());
            objCourse.LecturerId = user.Id;
            context.Course.Add(objCourse);
            context.SaveChanges();
            return RedirectToAction("Create", "Courses");
        }

